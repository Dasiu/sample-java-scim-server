package com.okta.scim.advice;

import com.google.gson.Gson;
import com.okta.scim.database.RequestDatabase;
import com.okta.scim.interceptors.LoggingInterceptor;
import com.okta.scim.models.Request;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.core.MethodParameter;
import org.springframework.http.HttpInputMessage;
import org.springframework.http.converter.HttpMessageConverter;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.servlet.mvc.method.annotation.RequestBodyAdvice;

import java.lang.reflect.Type;

/**
 * Controller advice for all requests for logging purposes
 */
@ControllerAdvice
public class LoggingRequestAdvice implements RequestBodyAdvice {
    RequestDatabase db;

    @Autowired
    public LoggingRequestAdvice(RequestDatabase db) {
        this.db = db;
    }

    @Override
    public boolean supports(MethodParameter methodParameter, Type type, Class<? extends HttpMessageConverter<?>> aClass) {
        return true;
    }

    @Override
    public HttpInputMessage beforeBodyRead(HttpInputMessage httpInputMessage, MethodParameter methodParameter, Type type, Class<? extends HttpMessageConverter<?>> aClass) {
        return httpInputMessage;
    }

    @Override
    public Object afterBodyRead(Object o, HttpInputMessage httpInputMessage, MethodParameter methodParameter, Type type, Class<? extends HttpMessageConverter<?>> aClass) {
        Request req = LoggingInterceptor.requests.get("Tomato");
        req.body = new Gson().toJson(o);

        db.save(req);

        System.out.println("afterBodyRead req saved");

        return o;
    }

    @Override
    public Object handleEmptyBody(Object o, HttpInputMessage httpInputMessage, MethodParameter methodParameter, Type type, Class<? extends HttpMessageConverter<?>> aClass) {
        return o;
    }
}
